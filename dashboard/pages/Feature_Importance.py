import streamlit as st
import pandas as pd
import pickle
import plotly.express as px

# Theme Colors

PRIMARY = "#2563EB"
SECONDARY = "#14B8A6"
ACCENT = "#F59E0B"

st.title("📊 Feature Importance")

st.markdown(
    """
Understand which features have the greatest influence on the predicted
Mumbai property price generated by the XGBoost model.
"""
)

# Load Model

with open("models/final_xgboost_model.pkl", "rb") as file:
    model = pickle.load(file)

# Feature Names

features = [
    "Year",
    "Price_per_sqm_Local",
    "Size_sqm",
    "Bedrooms",
    "Area_Bandra",
    "Area_Borivali",
    "Area_Colaba",
    "Area_Powai"
]

importance_df = pd.DataFrame({
    "Feature": features,
    "Importance": model.feature_importances_
})

importance_df = importance_df.sort_values(
    by="Importance",
    ascending=True
)

# Better Feature Labels

rename = {
    "Price_per_sqm_Local":"Price per Sq.m",
    "Size_sqm":"Property Size",
    "Bedrooms":"Bedrooms",
    "Year":"Construction Year",
    "Area_Bandra":"Bandra",
    "Area_Borivali":"Borivali",
    "Area_Colaba":"Colaba",
    "Area_Powai":"Powai"
}

importance_df["Feature"] = importance_df["Feature"].replace(rename)

# KPI Cards

top_feature = importance_df.iloc[-1]

least_feature = importance_df.iloc[0]

col1, col2, col3, col4 = st.columns(4)

with col1:
    st.metric("Features", len(importance_df))

with col2:
    st.metric("Most Important", top_feature["Feature"])

with col3:
    st.metric("Least Important", least_feature["Feature"])

with col4:
    st.metric("Model", "XGBoost")

st.divider()

# Interactive Plot

st.subheader("Feature Importance Ranking")

fig = px.bar(
    importance_df,
    x="Importance",
    y="Feature",
    orientation="h",
    color="Importance",
    color_continuous_scale="Blues",
    text="Importance"
)

fig.update_traces(
    texttemplate="%{text:.3f}",
    textposition="outside"
)

fig.update_layout(
    height=550,
    yaxis_title="",
    xaxis_title="Importance Score",
    plot_bgcolor="white",
    paper_bgcolor="white",
    coloraxis_showscale=False,
    font=dict(size=15)
)

st.plotly_chart(fig, use_container_width=True)

# Table

display = importance_df.copy()

display["Importance"] = display["Importance"].round(4)

st.subheader("Feature Importance Table")

st.dataframe(
    display,
    hide_index=True,
    use_container_width=True
)

st.divider()

# Business Interpretation

st.subheader("Business Interpretation")

for _, row in importance_df.sort_values("Importance", ascending=False).iterrows():

    if row["Importance"] > 0.30:
        st.success(f"**{row['Feature']}** is a major driver of property prices.")

    elif row["Importance"] > 0.10:
        st.info(f"**{row['Feature']}** has a moderate influence on the prediction.")

    else:
        st.warning(f"**{row['Feature']}** has relatively little influence on price prediction.")

st.divider()

# Model Insights

st.subheader("Model Insights")

st.markdown("""
### Key Observations

- Price per square meter is the strongest predictor of property value.

- Property size contributes significantly to overall pricing.

- Location features such as Bandra, Colaba and Powai strongly affect predictions.

- The construction year has comparatively lower influence than pricing and location variables.

- The XGBoost model automatically learns complex interactions between features, resulting in highly accurate predictions.

- Feature importance values indicate the relative contribution of each feature during model training.
""")